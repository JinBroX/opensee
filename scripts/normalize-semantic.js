function generateV3() {
  const v3Dir = path.join(BASE, 'v3');
  if (!fs.existsSync(v3Dir)) fs.mkdirSync(v3Dir, { recursive: true });
  let count = 0;
  for (const [id, data] of Object.entries(V3)) {
    const filePath = path.join(v3Dir, `${id}.json`);
    const json = {
      id,
      meta: {},
      segments: {
        status: data.status,
        trend: data.trend,
        mind: data.mind,
        risk: data.risk
      },
      lines: data.lines.map((text, i) => ({ line: i + 1, text })),
      summary: data.summary,
      closing: data.closing,
      version: 'v3'
    };
    fs.writeFileSync(filePath, JSON.stringify(json, null, 2), 'utf8');
    count++;
  }
  console.log(`v3 generated: ${count} files`);
}

