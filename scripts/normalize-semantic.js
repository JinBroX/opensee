function joinSegments(seg) {
  const out = {};
  for (const key of ['status', 'trend', 'mind', 'risk']) {
    const val = seg[key];
    if (!val || (Array.isArray(val) && val.length === 0)) {
      out[key] = '';
    } else if (Array.isArray(val)) {
      out[key] = val.join('');
    } else {
      out[key] = String(val);
    }
  }
  return out;
}

function deriveSummary(seg) {
  const statuses = Array.isArray(seg.status) ? seg.status : [seg.status || ''];
  const first = statuses[0] || '';
  // Use first status sentence as base, make it the summary
  const s = first.replace(/。$/, '');
  return s.length > 40 ? s.slice(0, 40) + '…' : s;
}

function deriveClosing(seg) {
  // Use risk content as basis for closing, or last trend
  const risks = Array.isArray(seg.risk) ? seg.risk : [seg.risk || ''];
  const trends = Array.isArray(seg.trend) ? seg.trend : [seg.trend || ''];
  const all = [...trends, ...risks].filter(Boolean);
  return all.length > 0 ? all[all.length - 1].replace(/。$/, '') : '';
}

function normalizeV2(filePath) {
  const raw = JSON.parse(fs.readFileSync(filePath, 'utf8'));
  const seg = joinSegments(raw.segments || {});
  return {
    id: raw.id,
    meta: {},
    segments: seg,
    lines: raw.lines || [],
    summary: deriveSummary(raw.segments || {}),
    closing: deriveClosing(raw.segments || {}),
    version: 'v2'
  };
}

function normalizeAllV2() {
  const v2Dir = path.join(BASE, 'v2');
  const files = fs.readdirSync(v2Dir).filter(f => f.endsWith('.json'));
  let count = 0;
  for (const f of files) {
    const filePath = path.join(v2Dir, f);
    try {
      const normalized = normalizeV2(filePath);
      fs.writeFileSync(filePath, JSON.stringify(normalized, null, 2), 'utf8');
      count++;
    } catch (e) {
      console.error(`  FAIL v2/${f}: ${e.message}`);
    }
  }
  console.log(`v2 normalized: ${count}/${files.length} files`);
}

